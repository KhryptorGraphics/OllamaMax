# Helper Functions
print_banner() {
    local text="$1"
    local width=80
    local padding=$(( (width - ${#text}) / 2 ))
    
    echo -e "\n${BLUE}$(printf 'â•%.0s' $(seq 1 $width))${NC}"
    echo -e "${BLUE}$(printf '%*s' $padding)${WHITE}$text${BLUE}$(printf '%*s' $padding)${NC}"
    echo -e "${BLUE}$(printf 'â•%.0s' $(seq 1 $width))${NC}\n"
}

print_section() {
    echo -e "\n${CYAN}â–¶ $1${NC}"
    echo -e "${CYAN}$(printf 'â”€%.0s' $(seq 1 50))${NC}"
}

print_success() {
    echo -e "${GREEN}âœ… $1${NC}"
    log_event "SUCCESS: $1"
}

print_error() {
    echo -e "${RED}âŒ $1${NC}"
    log_event "ERROR: $1"
}

print_warning() {
    echo -e "${YELLOW}âš ï¸  $1${NC}"
    log_event "WARNING: $1"
}

print_info() {
    echo -e "${CYAN}â„¹ï¸  $1${NC}"
    if $VERBOSE; then
        log_event "INFO: $1"
    fi
}

log_event() {
    echo "$(date '+%Y-%m-%d %H:%M:%S') - $1" >> "$LOG_FILE"
}

confirm_action() {
    local message="$1"
    local default="${2:-n}"
    
    if $DRY_RUN; then
        print_info "[DRY RUN] Would execute: $message"
        return 0
    fi
    
    if $FORCE_OVERWRITE; then
        return 0
    fi
    
    echo -e "${YELLOW}$message${NC}"
    read -p "Continue? (y/N): " -n 1 -r
    echo
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        return 1
    fi
    return 0
}

# Environment Setup Functions

setup_directory_structure() {
    print_section "Setting Up Directory Structure"
    
    local directories=(
        "$BASE_DIR"
        "$BASE_DIR/profiles"
        "$BASE_DIR/data"
        "$BASE_DIR/logs"
        "$BASE_DIR/scripts"
        "$BASE_DIR/exercises"
        "$BASE_DIR/solutions"
        "$BASE_DIR/certificates"
        "$BASE_DIR/backup"
        "$BASE_DIR/tools"
    )
    
    for dir in "${directories[@]}"; do
        if [[ ! -d "$dir" ]]; then
            if ! $DRY_RUN; then
                mkdir -p "$dir"
                chmod 755 "$dir"
            fi
            print_success "Created directory: $dir"
        else
            print_info "Directory exists: $dir"
        fi
    done
    
    # Create logs directory and initialize log file
    if ! $DRY_RUN; then
        touch "$LOG_FILE"
        chmod 644 "$LOG_FILE"
        log_event "Training environment setup started"
    fi
}

